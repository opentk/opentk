namespace OpenTK.NT.Native
{
    /// <summary>
    /// Specifies the color mode to use with color printers.
    /// </summary>
    public enum PrinterColor : short
    {
        /// <summary>
        /// Use monochrome printing mode.
        /// </summary>
        Monochrome = 1,

        /// <summary>
        /// Use color printing mode.
        /// </summary>
        Color = 2,
    }
}
