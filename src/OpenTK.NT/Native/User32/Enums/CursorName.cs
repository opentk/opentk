namespace OpenToolkit.NT.Native
{
    /// <summary>
    /// Specifies resource identifiers for the pre-defined cursors.
    /// </summary>
    public enum CursorName
    {
        /// <summary>
        /// Standard arrow and small hourglass.
        /// </summary>
        AppStarting = 32650,

        /// <summary>
        /// Standard arrow.
        /// </summary>
        Arrow = 32512,

        /// <summary>
        /// Crosshair.
        /// </summary>
        Cross = 32515,

        /// <summary>
        /// Hand.
        /// </summary>
        Hand = 32649,

        /// <summary>
        /// Arrow and question mark.
        /// </summary>
        Help = 32651,

        /// <summary>
        /// I-beam.
        /// </summary>
        IBeam = 32513,

        /// <summary>
        /// Obsolete for applications marked version 4.0 or later.
        /// </summary>
        Icon = 32641,

        /// <summary>
        /// Slashed circle.
        /// </summary>
        No = 32648,

        /// <summary>
        /// Obsolete for applications marked version 4.0 or later. Use <see cref="SizeAll"/>.
        /// </summary>
        Size = 32640,

        /// <summary>
        /// Four-pointed arrow pointing north, south, east, and west.
        /// </summary>
        SizeAll = 32646,

        /// <summary>
        /// Double-pointed arrow pointing northeast and southwest.
        /// </summary>
        SizeNortheastSouthwest = 32643,

        /// <summary>
        /// Double-pointed arrow pointing north and south.
        /// </summary>
        SizeNorthSouth = 32645,

        /// <summary>
        /// Double-pointed arrow pointing northwest and southeast.
        /// </summary>
        SizeNorthwestSoutheast = 32642,

        /// <summary>
        /// Double-pointed arrow pointing west and east.
        /// </summary>
        SizeWestEast = 32644,

        /// <summary>
        /// Vertical arrow.
        /// </summary>
        UpArrow = 32516,

        /// <summary>
        /// Hourglass.
        /// </summary>
        Wait = 32514
    }
}
