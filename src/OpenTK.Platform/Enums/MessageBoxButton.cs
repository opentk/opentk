#nullable enable

namespace OpenTK.Platform
{
    /// <summary>
    /// Represents a message box button.
    /// </summary>
    public enum MessageBoxButton
    {
        /// <summary>
        /// No button.
        /// </summary>
        None = 0,

        /// <summary>
        /// The "OK" button.
        /// </summary>
        Ok = 1,

        /// <summary>
        /// The "Yes" button.
        /// </summary>
        Yes = 2,

        /// <summary>
        /// The "No" button.
        /// </summary>
        No = 3,

        /// <summary>
        /// The "Cancel" button.
        /// </summary>
        Cancel = 4,

        /// <summary>
        /// The "Retry" button.
        /// </summary>
        Retry = 5,
    }
}
