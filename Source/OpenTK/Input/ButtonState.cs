namespace OpenTK.Input
{
    /// <summary>
    /// Enumerates possible mouse button states.
    /// </summary>
    public enum ButtonState
    {
        /// <summary>
        /// Indicates that a mouse button is released.
        /// </summary>
        Released = 0,

        /// <summary>
        /// Indicates that a mouse button is pressed.
        /// </summary>
        Pressed = 1
    }
}

